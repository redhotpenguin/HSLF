    <h4> Google Analytics</h4>

    <p class="indented">Log in to Google Analytics to see statistics about how your app is being used. View information about active users, sessions and most popular content. You will need your Google Analytics login information. Contact Winning Mark to add Google Analytics to your Gmail account.</p>
    
    <h4> App Store</h4>

    <p class="indented">Log in to Apple iTunes Connect to view and download sales and trends information, including download and update statistics for the iOS version of your app. You will need your iTunes login information.</p>

    <h4> Google Play</h4>

    <p class="indented">Log in to the Google Play Developer Console to view statistics about installs, uninstalls, active users and device type for the Android version of your app. You can also see ratings and reviews. You will need your Google Play login information.</p>
    
    <h4> Push Notifications History</h4>

    <p class="indented">Click to view a history of the push notifications you have sent. Click the "Stats" icon next to a notification to see more details, including the number sent, the number of direct responses, tags and action details. You can also edit the text of the message in the Alert Inbox or Delete the message from the Alert Inbox.</p>
